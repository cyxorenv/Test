# Some functions that work with numbers.
# =------------------------------------=

import math  #  <-- Calling a math Module for advanced math operations.

# Rounding a number.
print(round(2.9))
# Return the Absolute value of a number.
print(abs(-2.9))
# Find the ceiling of a number
print(math.ceil(2.2))

# Return x raised to the power y.
x = 5.0
y = 5.0
print(math.pow(x, y))

# Return the square root of x.
print(math.sqrt(y))
