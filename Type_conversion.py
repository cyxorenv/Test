# Here some examples of conversion

# int(x)   = converting x from any type to integer.
# float()  = converting x from any type to float.
# bool(x)  = converting x from any type to boolian True or False.
# str(x)   = converting x from any type to string.

# Example:

x = input("x: ")  # Getting from user a random number and store it in x.
y = float(x) + 1  # here we convert x (str) to integer value.
print(f"\nx: {x} \ny: {y}\n")  # Finnaly we print out the real type.

# You can use the same other types as above.
