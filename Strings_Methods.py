course = "  python programming"

# Make String all capital letters.
print(course.upper())
# Make string all lower-case letters.
print(course.lower())
# Make the 1st char in every word at the string as Capital.
print(course.title())
# Delete or ignore any white spaces.
print(course.strip())
# Find the index of letters.
print(course.find("Pro"))
# Replace a char or sequence of characters to something else.
print(course.replace("P", "X"))
# Checking existence of characters in the Strings True/False.
print("pro" in course)
